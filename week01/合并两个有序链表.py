"""
我用的遍历，看题解有遍历和递归两种。遍历和递归时间复杂度都是M+N, 遍历的空间复杂度是1，递归是M+N。尽量用循环遍历吧
"""


# Definition for singly-linked list.
class ListNode:
    def __init__(self, val=0, next=None):
        self.val = val
        self.next = next


class Solution:
    def mergeTwoLists(self, l1: ListNode, l2: ListNode) -> ListNode:
        if not l1:
            return l2
        if not l2:
            return l1
        l3 = head = ListNode()
        while l1 and l2:
            if l1.val < l2.val:
                l3.next = l1
                l1 = l1.next
            else:
                l3.next = l2
                l2 = l2.next
            l3 = l3.next
        l3.next = l1 if l1 else l2
        return head.next
